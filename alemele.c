void ShowTheTime(char coord_x, char coord_y, char h, char m, char s)
{
    char buffer[3];
    LCD_GoToLineColumn(coord_x, coord_y);
    if (h < 10)
    {
        lcd_putchar('0');
        LCD_GoToLineColumn(coord_x, coord_y+1);
    }
    itoa(h,buffer);
    lcd_puts(buffer);
    
    LCD_GoToLineColumn(coord_x, coord_y+2);
    lcd_putchar(':');
    LCD_GoToLineColumn(coord_x, coord_y+3);
    
    if (m < 10)
    {
        lcd_putchar('0');
        LCD_GoToLineColumn(coord_x, coord_y+4);
    }
    itoa(m,buffer);
    lcd_puts(buffer);
    
    LCD_GoToLineColumn(coord_x, coord_y+5);
    lcd_putchar(':');
    LCD_GoToLineColumn(coord_x, coord_y+6);
    
    if (s < 10)
    {
        lcd_putchar('0');
        LCD_GoToLineColumn(coord_x, coord_y+7);
    }
    itoa(s,buffer);
    lcd_puts(buffer);
    
}